-- CREATE THE STOCKS TABLE
CREATE TABLE pythor_stocks (
    id INT AUTO_INCREMENT PRIMARY KEY,
    stock_name VARCHAR(50) NOT NULL,
    stock_value INT NOT NULL,     
    last_updated DATETIME,            
    next_update DATETIME              
);

